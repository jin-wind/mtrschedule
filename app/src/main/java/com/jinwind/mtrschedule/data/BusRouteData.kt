    fun getRoute(routeNumber: String, isReverse: Boolean): BusRoute? {
        val routeList = routes[routeNumber]
        if (routeList.isNullOrEmpty()) return null
        
        // If isReverse is true, try to return the second route (index 1)
        // If isReverse is false, return the first route (index 0)
        val index = if (isReverse) 1 else 0
        if (index < routeList.size) {
            return routeList[index]
        }
        return routeList[0]
    }
